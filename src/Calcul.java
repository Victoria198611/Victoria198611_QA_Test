public class Calcul {
    public static void main(String[] args) {
        int n = 7;
        // if, else afisam daca numarul este pozitiv sau negativ
        if (n > 0) {
            System.out.println(n + " pozitiv ");
        } else if (n < 0) {
            System.out.println(n + " este negativ ");
        } else {
            System.out.println(n + " este egal cu 0 ");
        }
    }
}
